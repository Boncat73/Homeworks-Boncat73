# вкладені цикли (for внутри for)
# створення та виведення піраміди з зірочок
height = 5

# зовнішній цикл для кожного рівня піраміди
for row in range(1, height + 1):
    # внутрішній цикл для виведення зірочок на поточному рівні
    for column in range(row):
        print("*", end='') # виводимо зірочку без переходу на новий рядок
    print() # після завершення внутрішнього циклу переходимо на новий рядок