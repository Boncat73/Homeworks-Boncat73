# Відображення відсотків
# Запитайте у користувача чисельник і знаменник. Обчисліть відсоток і 
# відобразіть його з двома знаками після коми, після яких ставиться 
# знак відсотка (наприклад, 75,50%).
def calculate_and_display_percentage():
    """
    Запитує чисельник та знаменник, обчислює відсоток 
    і виводить його з двома знаками після коми та знаком відсотка. 
    Обробляє помилки нечислового введення та поділу на нуль.
    """
    
    # Цикл для забезпечення коректного вводу
    while True:
        try:
            # 1. отримання вводу та перетворення у число з крапкою
            numerator = float(input("Enter the numerator (Числитель): "))
            denominator = float(input("Enter the denominator (Знаменатель): "))
            
            # 2. перевірка ділення на нуль
            if denominator == 0:
                print("\nПомилка,знаменник не може бути нулем.Будь ласка,введіть інше число.")
                # повертаємось у начало циклу
                continue
            
            # 3. розрахунок відсотків
            # Формула: (чисельник / знаменник) * 100
            percentage = (numerator / denominator) * 100
            
            # 4. виводимо результат у тому форматі,в якому потрібно
            # використовується f-строка:
            # :.2f - фортамування числа до двох знаків після коми (Fixed point notation)
            # % - знак проценту
            print(f"\nРезультат:")
            print(f"({numerator} / {denominator}) * 100 = {percentage:.2f}%")
            
            # якщо все вдалось,то виходимо з циклу
            break
            
        except ValueError:
            # обробка випадку,коли не число
            print("\nПомилка,будь ласка,надавайте тільки числа.")
            # повертаємось до начала циклу
            continue

# запуск функції
calculate_and_display_percentage()

