# Створіть просте інтерактивне меню з такими опціями, як 
# «1. Привітатися», 
# «2. Обчислити квадрат», 
# «3. Вихід». 
# На основі введених користувачем даних виконайте відповідну дію.

def greet():
    # виводить привітання
    print('\nПривіт!Радий Вас бачити в цьому інтерактивному меню.')

def calculate_square():
    # обчислює квадрат числа,введеного користувачем
    while True:
        try:
            # запит числа у користувача
            number_str = input('\nВведіть число для обчислення квадрату: ')
            # перевірка, чи не порожній рядок ввів користувач
            if not number_str:
                print('Ви нічого не ввели.Будь ласка,введіть число.')
                continue

            number = float(number_str)
            square = number ** 2 # обчислення квадрату

            # виведення результату з форматуванням (наприклад, 2.0 -> 4.0)
            # якщо число ціле,виводимо його як ціле,інакше виводимо з комою
            if number == int(number):
                print(f'Квадрат числа {int(number)} дорівнює {square}.')
            else:
                print(f'Квадрат числа {number} дорівнює {square}.')
            break # вихід з внутрішнього циклу після успішного обчислення
        except ValueError:
            # обробка помилки,якщо введено не числове значення
            print('Не коректний ввід.Будь ласка,введіть дійсне число.')

def display_menu():
    # виводить опції меню
    print('\n' + '=' * 30)
    print('   ГОЛОВНЕ МЕНЮ')
    print('=' * 30)
    print('1.Привітатися')
    print('2.Обчислити квадрат')
    print('3.Вихід')
    print('=' * 30)

def main():
    # основна функція,яка керує інтерактивним меню
    # головний цикл програми
    while True:
        display_menu()
        # запит вибору користувача
        choice = input('Введіть номер опції (1, 2 або 2): ').strip()
        # обробка вибору користувача
        if choice == '1':
            greet()
        elif choice == '2':
            calculate_square()
        elif choice == '3':
            # опція "Вихід"
            print('\nДякую за використання меню! До побачення.')
            break # вихід із головного циклу while True
        else:
            # обробка не коректного вводу
            print(f'\nНевідома опція {choice}. Будь ласка, введіть 1, 2 або 3')

# запуск програми
if __name__ == '__main__':
    main()

