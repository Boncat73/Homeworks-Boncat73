# .isdigit() - це вбудований метод рядків,який використовується для перевірки,
# чи складається рядок виключно з цифрових символів
def zero_padding_output():
    """
    Запитує у користувача число і виводить його, 
    доповнене початковими нулями до загальної ширини 5.
    """
    
    # Цикл для забезпечення коректного введення числа
    while True:
        user_input = input("Введіть ціле число (наприклад, 45 або 1234): ").strip()
        
        # Перевірка, чи ввід складається лише з цифр
        if not user_input.isdigit():
            print("Некоректний ввід. Будь ласка, введіть ціле число.")
            continue
        
        # Перетворення введеного рядка на ціле число (для прикладу його можна залишити і рядком)
        number = int(user_input)
        
        # --- Основне форматування ---
        
        # Специфікація формату:
        # 05 - означає, що загальна ширина поля має бути 5 символів, 
        #      і вільне місце потрібно заповнити нулями (0).
        padded_number = f"{number:05}"
        
        # --- Вивід результату ---
        print("\n" + "="*30)
        print("Результат доповнення нулями:")
        print(f"Введене число: {number}")
        print(f"Доповнене число: {padded_number}")
        print("="*30)
        
        break

# Запуск функції
zero_padding_output()