# Вивести вирівняний по правому краю вивід
# Запитати користувача про слово та число. Вивести слово, 
# вирівняне по правому краю, у поле шириною 20, а потім число.

def right_align_output():
    """
    Запитує слово та число у користувача. 
    Виводить слово, вирівняне по правому краю в полі шириною 20, 
    а потім виводить число.
    """
    
    # --- 1. Отримання слова ---
    word = input("Введіть слово (наприклад, Python): ").strip()
    
    # --- 2. Отримання числа з перевіркою (для надійності) ---
    while True:
        try:
            number_input = input("Введіть число (наприклад, 123.45): ")
            number = float(number_input) # Перетворюємо в float, щоб приймати десяткові числа
            break
        except ValueError:
            print("Некоректний ввід. Будь ласка, введіть дійсне число.")
    
    # --- 3. Форматування та вивід ---

    # Створення f-рядка для форматування
    # Специфікація форматування для слова: 
    # >20 означає: вирівняти по правому краю (>) у полі шириною 20 символів.
    
    formatted_output = f"Слово: {word:>20} | Число: {number}"
    
    print("\n" + "="*45)
    print("Вивід, вирівняний по правому краю:")
    print(formatted_output)
    print("="*45)
    
    # Додатковий вивід для демонстрації ширини поля
    print("\n(Для наочності, ось поле шириною 20 символів, обмежене символами '|' та '*'):")
    # Використання . для заповнення лівого простору
    print(f"|{word:>20}|") 
    print(f"|{'*'*20}|") 

# Запуск функції
right_align_output()

