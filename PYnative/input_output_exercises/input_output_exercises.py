# Доповнення нулями.Запитайте у користувача число. Виведіть це число, доповнене 
# початковими нулями шириною 5.Наприклад, якщо введено 12, вивід має бути «00012».
# .zfill() - вбудований метод рядків для доповнення 
# рядка нулями зліва до заданої загальної довжини 

# number_str = input("Enter a number: ")
