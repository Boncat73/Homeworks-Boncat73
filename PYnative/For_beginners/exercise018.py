# треба перекласти правила григоріанського календаря на мову програмування
# визначення високосного року
def check_leap_year():
    # визначення року від користувача
    try:
        year = int(input("Введіть рік для перевірки: "))
        
        # Правило визначення високосного року:
        # Рік є високосним, якщо він кратний 4, але не кратний 100,
        if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0):
            print(f"{year} — це високосний рік (366 днів).")
        else:
            print(f"{year} — це не високосний рік.")
            
    except ValueError:
        print("Будь ласка, введіть ціле число.")

print("-" * 30)

# Виклик функції для перевірки року
check_leap_year()

